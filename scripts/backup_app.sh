#!/bin/bash

# ============================================================
# 부부신호등(Heart Sync) 소스 코드 백업 스크립트
# ============================================================
# 이 스크립트는 어플의 기능, 디자인, 로직이 담긴 핵심 소스 코드를 
# 압축하여 백업 파일(.zip)로 만들어줍니다.
# 개인 데이터(데이터베이스 내 정보)는 포함하지 않습니다.

# 백업 파일명 (현재 시간 포함)
BACKUP_NAME="HeartSync_Backup_$(date +%Y%m%d_%H%M%S).zip"

echo "📦 백업을 시작합니다: $BACKUP_NAME"

# 핵심 파일 및 폴더 압축 (불필요한 대용량 파일 제외)
zip -r "$BACKUP_NAME" . \
  -x "node_modules/*" \
  -x ".git/*" \
  -x ".DS_Store" \
  -x "*.zip" \
  -x "dist/*" \
  -x "build/*"

if [ $? -eq 0 ]; then
  echo "✅ 백업이 성공적으로 완료되었습니다!"
  echo "파일 위치: $(pwd)/$BACKUP_NAME"
else
  echo "❌ 백업 중 오류가 발생했습니다."
fi
